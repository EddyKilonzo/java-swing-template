    private void reports_selection_listValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_reports_selection_listValueChanged
        if (reports_selection_list.getSelectedIndex() > -1) {
            SystemReport sysrp = (SystemReport) reports_selection_list.getSelectedValue();
            reportdesc.setText("<html>" + sysrp.getReportDescription());
        } else {
            reportdesc.setText("Please select a report to see the description.");
        }
    }//GEN-LAST:event_reports_selection_listValueChanged

    private void reports_selection_listMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reports_selection_listMouseClicked
        if (evt.getClickCount() > 1) {
            if (reports_selection_list.getSelectedIndex() > -1) {
                SystemReport sysrp = (SystemReport) reports_selection_list.getSelectedValue();
                showReport(sysrp);
            }
        }
    }//GEN-LAST:event_reports_selection_listMouseClicked

    private void reports_selection_listKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_reports_selection_listKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            evt.consume();
            if (reports_selection_list.getSelectedIndex() > -1) {
                SystemReport sysrp = (SystemReport) reports_selection_list.getSelectedValue();
                showReport(sysrp);
            }
        }
    }//GEN-LAST:event_reports_selection_listKeyPressed

