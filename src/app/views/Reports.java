    private void fillReportCategories() {
        DefaultComboBoxModel def = (DefaultComboBoxModel) cmb_report_types.getModel();
        ReportType[] rtypes = ReportType.values();
        for (ReportType rtype : rtypes) {
            def.addElement(rtype);
        }
    }

    private void loadReportsForCategory() {
        if (cmb_report_types.getSelectedIndex() == -1) {
            return;
        }
        ArrayList<SystemReport> rlist = new ArrayList<>();
        ReportType rty = (ReportType) cmb_report_types.getSelectedItem();
        SystemReport[] reports = SystemReport.values();
        for (SystemReport sysrp : reports) {
            if (sysrp.getRtype().equals(rty) && sysrp.isActive()) {
                rlist.add(sysrp);
            }
        }
        reports_selection_list.setListData(rlist.toArray());
    }

    private void _viewReport(final SystemReport systemReport, final HashMap<String, Object> params) {
        try {
            new Thread(new Runnable() {

                @Override
                public void run() {
                    setGlassPane(cover_overlay);
                    getGlassPane().setVisible(true);
                    gen_report.setEnabled(false);
                    ReportsController.prepareAndViewReport(systemReport, params);
                    gen_report.setEnabled(true);
                    getGlassPane().setVisible(false);
                }
            }).start();
        } catch (Exception e) {
            getGlassPane().setVisible(false);
        }
    }

    private void _viewReport(final SystemReport systemReport, final HashMap<String, Object> params, final Callback runBefore) {
        try {
            new Thread(new Runnable() {

                @Override
                public void run() {
                    setGlassPane(cover_overlay);
                    getGlassPane().setVisible(true);
                    gen_report.setEnabled(false);
                    runBefore.run();
                    ReportsController.prepareAndViewReport(systemReport, params);
                    gen_report.setEnabled(true);
                    getGlassPane().setVisible(false);
                }
            }).start();
        } catch (Exception e) {
            getGlassPane().setVisible(false);
        }
    }


