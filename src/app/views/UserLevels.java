    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (editing_ != null) {
            editing_.setLevelCode(lcode.getText().trim());
            editing_.setLevelName(lname.getText().trim());
            editing_.setLevelDisplayName(ldisplayname.getText().trim());
            editing_.setIsActiveLevel(levelactive.isSelected());
            boolean updateUserLevel = UserController.updateUserLevel(editing_);
            if (updateUserLevel) {
                reset_();
            }
        } else {
            UserLevel ul = new UserLevel();
            ul.setLevelCode(lcode.getText().trim());
            ul.setLevelName(lname.getText().trim());
            ul.setLevelDisplayName(ldisplayname.getText().trim());
            ul.setIsActiveLevel(levelactive.isSelected());
            boolean saveUserLevel = UserController.saveUserLevel(ul);
            if (saveUserLevel) {
                reset_();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

