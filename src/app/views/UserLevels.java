    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        if (editing_ != null) {
            boolean deleteUserLevel = UserController.deleteUserLevel(editing_);
            if (deleteUserLevel) {
                reset_();
            }
        }
    }//GEN-LAST:event_UpdateActionPerformed

    private void jXTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jXTable1MouseClicked
        if (evt.getClickCount() >= 2) {
            int sr = jXTable1.getSelectedRow();
            if (sr != -1) {
                UserLevel ull = (UserLevel) jXTable1.getValueAt(sr, 0);
                if (ull != null) {
                    editing_ = ull;
                    lcode.setText(ull.getLevelCode());
                    lname.setText(ull.getLevelName());
                    ldisplayname.setText(ull.getLevelDisplayName());
                    levelactive.setSelected(ull.isActiveLevel());
                    jButton4.setText("Update");
                }
            }
        }
    }//GEN-LAST:event_jXTable1MouseClicked

